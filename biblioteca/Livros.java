package template.biblioteca;

public class Livros {
    String titulo;
    String autor;
    String ano;
    String genero;
    String numeroPaginas;
 
    public Livros(String titulo, String autor, String ano, String genero, String numeroPaginas) {
        this.titulo = titulo;
        this.autor = autor;
        this.ano = ano;
        this.genero = genero;
        this.numeroPaginas = numeroPaginas;
    }

}
